package org.air.bigearth.apps.system.domain.basic;

/**
 * 角色表 apps_t_sys_role
 *
 * @author wangxuming
 * @version 1.0
 * @date 2019-04-24
 */
public class Role implements Comparable<Role>{
    private String id;

    private String name;

    private String code;

    private Byte sort;

    private Byte type;

    private String des;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Byte getSort() {
        return sort;
    }

    public void setSort(Byte sort) {
        this.sort = sort;
    }

    public Byte getType() {
        return type;
    }

    public void setType(Byte type) {
        this.type = type;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

	@Override
	public int compareTo(Role o) {
		return this.getSort().compareTo(o.getSort());
	}
}